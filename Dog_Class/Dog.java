
public class Dog {
	
	private int size;
	private String bread;
	private String name;
	
	public void bark() {
		
		System.out.println("Ruff! Ruff!");
		
	}

	public int getSize() {
		return size;
	}

	public void setSize(int size) {
		this.size = size;
	}

	public String getBread() {
		return bread;
	}

	public void setBread(String bread) {
		this.bread = bread;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
